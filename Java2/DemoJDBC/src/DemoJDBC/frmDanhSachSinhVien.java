    public void HienthiDSSV(){
        Connection cnn = CSDL.Database.KetnoiCSDL();
        if(cnn==null){
            JOptionPane.showMessageDialog(rootPane,"Loi ket noi CSDL");
            return;
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"Ket noi CSDL thanh cong");
            String sql = "SELECT * FROM tbSinhvien";
            try{
                //thuc thi sql va lay ket qua
                PreparedStatement stm = cnn.prepareStatement(sql);
                //thuc hien lenh sql va luu ket qua vao resultset
                ResultSet rs = stm.executeQuery();
                DefaultTableModel dtm = (DefaultTableModel) tbSinhvien.getModel();
                //xoa du lieu neu co
                dtm.setRowCount(0);
                
                int dem =0;
                while(rs.next()){
                    String Masv = rs.getString("Masv");
                    String Hoten = rs.getString("Hoten");
                    String Dienthoai = rs.getString("Dienthoai");
                    dtm.addRow(new Object[]{Masv, Hoten, Dienthoai});
                    dem++;
                }
                if(dem==0){
                    JOptionPane.showMessageDialog(this,"Chua co du lieu");
                }
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Loi truy van ");
                Logger.getLogger(frmDanhSachSinhVien.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }
