    private void btnXoasinhvienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoasinhvienActionPerformed
        // TODO add your handling code here:
        int i = tbSinhvien.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(this,"Ban chua chon sinh vien can xoa");
            return;
        }
        DefaultTableModel dtm = (DefaultTableModel) tbSinhvien.getModel();
        //lay du lieu tu dong i, cot 0 (ma sinh vien)
        String masv = (String)dtm.getValueAt(i, 0);
        int res = JOptionPane.showConfirmDialog(this,"Xoa sinh vien", "Chac chan xoa",JOptionPane.YES_NO_OPTION);
        if(res==JOptionPane.YES_OPTION);{
            Connection cnn = CSDL.Database.KetnoiCSDL();
            if(cnn==null){
                JOptionPane.showMessageDialog(this,"Loi ket noi CSDL");
                return;
            }
            String sql = "DELETE FROM tbSinhvien WHERE masv = ?";
            try{
                PreparedStatement stm = cnn.prepareStatement(sql);
                stm.setString(1, masv);
                int n = stm.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(this,"Xoa thanh cong");
                    HienthiDSSV();
                }
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(this,"Loi SQL!");
                Logger.getLogger(frmDanhSachSinhVien.class.getName()).log(Level.SEVERE, null, e);
            }
            
        }
    }//GEN-LAST:event_btnXoasinhvienActionPerformed

